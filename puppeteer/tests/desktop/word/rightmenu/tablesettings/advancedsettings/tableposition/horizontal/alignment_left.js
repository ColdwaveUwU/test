// Test for table horizontal alignment left settings in table properties
const { Table, FileMenu, Verification } = require("lib");
Tester.createFile("docx");
Table.insertTable(3, 4);
Table.setTableAdvancedSettings({
    textWrapping: {
        wrappingStyle: "parallel",
    },
    tablePosition: {
        horizontal: { alignment: { type: "Left" } },
    },
});
FileMenu.downloadAs("docx");
// Getting verification results
Verification.openFile();
Verification.check("word/document.xml", "//w:tblpPr[1]/@w:tblpXSpec", "left");
let isSuccess = Verification.isSuccess();
console.log(isSuccess);

Tester.close();
